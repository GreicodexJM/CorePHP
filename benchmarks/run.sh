#!/usr/bin/env sh
# =============================================================================
# CorePHP benchmark runner — persistent (CorePHP) vs cold-start (PHP-FPM).
#
#   ./run.sh                      # default: 4 threads, 50 conns, 15s
#   DURATION=30s CONNS=100 ./run.sh
#
# Requires: docker, and the corephp-vm:latest image (build it from repo root:
#   make build   — or —   docker build -t corephp-vm:latest .
# The load generator (wrk) runs in a container; nothing else is needed on the host.
# =============================================================================
set -eu

cd "$(dirname "$0")"

DURATION="${DURATION:-15s}"
CONNS="${CONNS:-50}"
THREADS="${THREADS:-4}"
WARMUP="${WARMUP:-3}"
WRK_IMAGE="${WRK_IMAGE:-williamyeh/wrk}"
COMPOSE="docker compose -f docker-compose.bench.yaml"

COREPHP_URL="http://localhost:8090/"
FPM_URL="http://localhost:8091/"

GREEN='\033[0;32m'; RED='\033[0;31m'; DIM='\033[2m'; NC='\033[0m'

cleanup() { $COMPOSE down --remove-orphans >/dev/null 2>&1 || true; }
trap cleanup EXIT

if ! docker image inspect corephp-vm:latest >/dev/null 2>&1; then
    printf "${RED}✗ corephp-vm:latest not found.${NC} Build it first: (cd .. && make build)\n"
    exit 1
fi

printf "→ Pulling load generator (%s) …\n" "$WRK_IMAGE"
docker pull "$WRK_IMAGE" >/dev/null

printf "→ Starting benchmark stack (building PHP-FPM baseline) …\n"
$COMPOSE up -d --build >/dev/null

# --- wait for both to serve 200 -------------------------------------------
wait_ready() {
    name="$1"; url="$2"
    i=0
    while [ "$i" -lt 30 ]; do
        code=$(curl -s -o /dev/null -w "%{http_code}" "$url" 2>/dev/null || echo 000)
        [ "$code" = "200" ] && { printf "  ${GREEN}✓${NC} %s ready\n" "$name"; return 0; }
        i=$((i + 1)); sleep 1
    done
    printf "  ${RED}✗ %s never returned 200 (last: %s)${NC}\n" "$name" "$code"
    $COMPOSE logs "$3" 2>&1 | tail -15
    exit 1
}
printf "→ Waiting for services …\n"
wait_ready "CorePHP" "$COREPHP_URL" corephp
wait_ready "PHP-FPM" "$FPM_URL" nginx

# --- correctness: both runtimes must produce identical output --------------
printf "→ Verifying identical output (drop-in proof) …\n"
a=$(curl -s "$COREPHP_URL"); b=$(curl -s "$FPM_URL")
if [ "$a" = "$b" ]; then
    printf "  ${GREEN}✓ byte-identical responses${NC} ${DIM}(%s bytes)${NC}\n" "${#a}"
else
    printf "  ${RED}✗ responses differ — aborting (not apples-to-apples)${NC}\n"
    exit 1
fi

# --- load test -------------------------------------------------------------
run_wrk() {  # name url -> writes /tmp/bench.$name
    name="$1"; url="$2"
    printf "→ Load testing %s ${DIM}(%s threads, %s conns, %s; %ss warmup)${NC} …\n" \
        "$name" "$THREADS" "$CONNS" "$DURATION" "$WARMUP"
    docker run --rm --network host "$WRK_IMAGE" \
        -t"$THREADS" -c"$CONNS" -d"${WARMUP}s" "$url" >/dev/null 2>&1 || true   # warmup
    docker run --rm --network host "$WRK_IMAGE" \
        -t"$THREADS" -c"$CONNS" -d"$DURATION" --latency "$url" 2>/dev/null > "/tmp/bench.$name"
}
run_wrk corephp "$COREPHP_URL"
run_wrk fpm "$FPM_URL"

# --- parse wrk output ------------------------------------------------------
# Requests/sec:  NNNN.NN   |   Latency Distribution: "     50%    7.13ms"
rps()  { awk '/Requests\/sec:/ {print $2}' "$1"; }
p()    { awk -v pct="$1" '$1==pct {print $2}' "$2"; }   # pct e.g. "50%"

CP_RPS=$(rps /tmp/bench.corephp); FPM_RPS=$(rps /tmp/bench.fpm)
CP_P50=$(p "50%" /tmp/bench.corephp); FPM_P50=$(p "50%" /tmp/bench.fpm)
CP_P90=$(p "90%" /tmp/bench.corephp); FPM_P90=$(p "90%" /tmp/bench.fpm)
CP_P99=$(p "99%" /tmp/bench.corephp); FPM_P99=$(p "99%" /tmp/bench.fpm)

speedup=$(awk -v a="$CP_RPS" -v b="$FPM_RPS" 'BEGIN{ if (b>0) printf "%.1f", a/b; else print "n/a" }')

# --- write RESULTS.md ------------------------------------------------------
{
  echo "# Benchmark Results"
  echo
  echo "> Auto-generated by \`run.sh\`. Do not edit by hand."
  echo
  echo "Persistent (**CorePHP** / RoadRunner) vs cold-start-per-request (**PHP-FPM** + nginx, opcache on),"
  echo "both serving the **identical** application. The app has a framework-style \`bench_bootstrap()\`"
  echo "phase (config tree, route compilation, service container): CorePHP runs it **once per worker**,"
  echo "PHP-FPM runs it **on every request** (its process dies each time). Higher req/s and lower latency are better."
  echo
  echo "| Runtime | Requests/sec | Latency p50 | p90 | p99 |"
  echo "|---|--:|--:|--:|--:|"
  echo "| **CorePHP** (persistent) | ${CP_RPS:-?} | ${CP_P50:-?} | ${CP_P90:-?} | ${CP_P99:-?} |"
  echo "| PHP-FPM (cold start) | ${FPM_RPS:-?} | ${FPM_P50:-?} | ${FPM_P90:-?} | ${FPM_P99:-?} |"
  echo
  echo "**CorePHP throughput: ${speedup}× PHP-FPM.**"
  echo
  echo "<sub>Config: ${THREADS} threads, ${CONNS} connections, ${DURATION} (after ${WARMUP}s warmup)."
  echo "Both runtimes verified to return byte-identical responses. Single-machine, synthetic workload —"
  echo "reproduce with \`./run.sh\`; absolute numbers vary by hardware, the ratio is the point.</sub>"
} > RESULTS.md

printf "\n${GREEN}=== Results ===${NC}\n"
cat RESULTS.md
printf "\n${GREEN}✓ Written to benchmarks/RESULTS.md${NC}\n"
